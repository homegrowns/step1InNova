package com.example.honeybee;

public class MemberData {

    private String member_phone;
    private String member_name;
    private String member_BirthDate;
    private String member_sex;
    private String member_hobby;
    private String member_EmailAddress;

    public String getMember_phone() {
        return member_phone;
    }

    public void setMember_phone(String member_phone) {
        this.member_phone = member_phone;
    }

    public String getMember_name() {
        return member_name;
    }

    public void setMember_name(String member_name) {
        this.member_name = member_name;
    }

    public String getMember_BirthDate() {
        return member_BirthDate;
    }

    public void setMember_BirthDate(String member_BirthDate) {
        this.member_BirthDate = member_BirthDate;
    }

    public String getMember_sex() {
        return member_sex;
    }

    public void setMember_sex(String member_sex) {
        this.member_sex = member_sex;
    }

    public String getMember_hobby() {
        return member_hobby;
    }

    public void setMember_hobby(String member_hobby) {
        this.member_hobby = member_hobby;
    }

    public String getMember_EmailAddress() {
        return member_EmailAddress;
    }

    public void setMember_EmailAddress(String member_EmailAddress) {
        this.member_EmailAddress = member_EmailAddress;
    }
}
