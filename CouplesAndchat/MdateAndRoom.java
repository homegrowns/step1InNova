package CouplesAndchat;

public class MdateAndRoom {

    private String Mday;
    private String Room;

    public MdateAndRoom(String mday, String room) {
        Mday = mday;
        Room = room;
    }

    public String getMday() {
        return Mday;
    }

    public void setMday(String mday) {
        Mday = mday;
    }

    public String getRoom() {
        return Room;
    }

    public void setRoom(String room) {
        Room = room;
    }
}
